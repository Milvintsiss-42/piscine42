#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	print_char_as_hex(unsigned char c)
{
	char	 *base;

	base = "0123456789abcdef";
	ft_putchar('\\');
	ft_putchar(base[c / 16]);
	ft_putchar(base[c % 16]);
}

void	ft_putstr_non_printable(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= ' ' && str[i] <= '~')
			ft_putchar(str[i]);
		else
			print_char_as_hex(str[i]);
		i++;
	}
}
